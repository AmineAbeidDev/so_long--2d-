#include "map_utils.h"

int	ft_strmchr(const char *str, int c)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((char)str[i] == (char)c)
			return (i);
		i++;
	}
	return (0);
}
